-- Toggle comments.
vim.keymap.set('n', '<leader>/', 'gcc', { desc = 'toggle comment', remap = true })
vim.keymap.set('v', '<leader>/', 'gc', { desc = 'toggle comment', remap = true })

-- Close the current buffer with confirmation if changed.
local close_buf_and_switch = function()
  vim.cmd('bdelete')
  -- Switch to the previous buffer if one exists.
  if #vim.api.nvim_list_bufs() > 0 then
    vim.cmd('bprevious')
  end
end
vim.keymap.set('n', '<leader>x', close_buf_and_switch, { desc = '[X] Close current buffer' })

